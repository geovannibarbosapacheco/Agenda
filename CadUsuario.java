    public void consultar(){
    String sql  ="select * from usuario where id=?";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, TxtIdUser.getText());
            rs = pst.executeQuery();
            
            if(rs.next()){
            TxtNomeUser.setText(rs.getString(2));
            TxtUserUser.setText(rs.getString(3));
            TxtSenhaUser.setText(rs.getString(4));
            CaixaGrauAcessoUser.setSelectedItem(rs.getString(5));
            }else{
            JOptionPane.showMessageDialog(null,"Usuario n√£o encontrado");
            
            TxtNomeUser.setText(null);
            TxtUserUser.setText(null);
            TxtSenhaUser.setText(null);
            CaixaGrauAcessoUser.setSelectedItem(null);
            
            }
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    
    public void adicionar(){
    String sql = "insert into usuario(id, nome, userUsuario, senha, graudeacesso) values(?,?,?,?,?) ";
        try {
            pst = conexao.prepareStatement(sql);
            pst.setString(1, TxtIdUser.getText());
            pst.setString(2, TxtNomeUser.getText());
            pst.setString(3, TxtUserUser.getText());
            pst.setString(4, TxtSenhaUser.getText());
            pst.setString(5, CaixaGrauAcessoUser.getSelectedItem().toString());
            
            pst.executeUpdate();
            
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
            
        }
    
    }
    

